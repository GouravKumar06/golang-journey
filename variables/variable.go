package main

func main() {
	// bool
	var isPasswordMatched bool = true
	println("isPasswordMatched", isPasswordMatched)

	var isPassed = false
	println("isPassed", isPassed)

	// complex
	var complexNumber complex64 = 1 + 2i
	println("complexNumber", complexNumber)

	var complexNumber2 complex128 = 1 + 2i
	println("complexNumber2", complexNumber2)

	// float32
	var price float32 = 3.14
	println("price", price)

	var simpleInterest = 876.99
	println("simpleInterest", simpleInterest)


	// string
	var name string = "John Doe"
	println("name", name)

	var currentTab = "Home"
	println("currentTab", currentTab)

	var friend_name string
	println("friend_name", friend_name)

	friend_name = "John Doe"
	println("friend_name after assignment", friend_name)

	// int
	var age int = 20
	println("age", age)

	var currentYear = 2025
	println("currentYear", currentYear)

}